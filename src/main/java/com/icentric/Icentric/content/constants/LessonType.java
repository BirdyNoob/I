package com.icentric.Icentric.content.constants;

/**
 * Defines the four fixed lesson types that make up the standardized
 * curriculum sequence inside every CourseModule:
 *
 *  1. VIDEO_CONCEPT       – Intro video + key takeaways (sortOrder 1)
 *  2. INTERACTIVE_SCENARIO – Scenario-based learning activity  (sortOrder 2)
 *  3. DOS_AND_DONTS       – Comparative best-practice slides  (sortOrder 3)
 *  4. QUIZ                – Knowledge-check assessment        (sortOrder 4)
 */
public enum LessonType {
    VIDEO_CONCEPT,
    INTERACTIVE_SCENARIO,
    DOS_AND_DONTS,
    QUIZ
}
